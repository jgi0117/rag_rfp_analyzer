def make_ground_truth_dataframe(document_ids: Optional[Iterable[str]] = None) -> pd.DataFrame:
    """선택한 문서들의 평가 질문을 하나의 DataFrame으로 반환한다."""
    selected_ids = list(document_ids) if document_ids is not None else list(GROUND_TRUTH_REGISTRY)
    rows = []

    for document_id in selected_ids:
        if document_id not in GROUND_TRUTH_REGISTRY:
            raise KeyError(f"Unknown ground truth document_id: {document_id}")

        entry = GROUND_TRUTH_REGISTRY[document_id]
        for row in entry["rows"]:
            rows.append(
                {
                    "document_id": document_id,
                    "document_name": entry["document_name"],
                    **row,
                }
            )

    return pd.DataFrame(rows)
